<?php
class Circle
{
    public $radius;
    public $name;
    public function __construct($radius,$name)
    {
        $this->radius;
        $this->name;
    }
    public function getName()
    {
        return $this->name;
    }
    public function setName()
    {
        $this->name;
    }
    public function getRadius()
    {
        return $this->radius;
    }
    public function setRadius()
    {
        $this->radius;
    }

}